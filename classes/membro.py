class Membro:

    def __init__(self, nome, sobrenome, data_nascimento, endereco, numero, email, tipodoplano, data_inicio, ativo, id = None):
        self.nome = nome
        self.sobrenome = sobrenome
        self.data_nascimento = data_nascimento
        self.endereco = endereco
        self.numero = numero
        self.email = email
        self.tipodoplano = tipodoplano
        self.data_inicio = data_inicio
        self.ativo = ativo
        self.id = id
